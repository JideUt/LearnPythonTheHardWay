# Word Drills
# class - Tell Python to make a new kind of thing.
# object - Two meanings: the most basic kind of thing and any instance of a thing
# instance - What you get when you tell Python to create a class
# def - How you define a function
# self - inside the functions in a class, self is a variable for the instance/ object
#     being accessed.
# inheritance - The concept that one class can inherit traits from another class
#     much like you and your parents
# composition - the concept that a class can be composed of other classes as parts
#     much like how a car has wheels
# attribute - A property classes have that are from composition and are usually
#     variables
# is-a - A phrase to say that something inherits from another, as in
#     a "'salmon' is-a 'fish'"
# has-a - A phrase to say that something is composed of other things or has
#     a trait, as in "a 'salmon' has-a 'mouth'"
